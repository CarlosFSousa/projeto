class Aeroporto:

    def __init__(self, id, nome, cidade, pais, iata, icao, longitude, latitude, altitude, dst, tz):
        self.id = id
        self.nome = nome
        self.cidade = cidade
        self.pais = pais
        self.iata = iata
        self.icao = icao
        self.longitude = longitude
        self.latitude = latitude
        self.altitude = altitude
        self.dst = dst
        self.tz = tz

